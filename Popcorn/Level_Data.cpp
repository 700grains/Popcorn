ALevel_Data::ALevel_Data(int level_number)
	: Level(nullptr)
{
	if (level_number < 1 || level_number > Max_Level_Number)
		AsConfig::Throw();

	switch (level_number)
	{
	case 1:
		Level = (char*)Level_01;
		break;

	case 2:
		Level = (char*)Level_02;
		break;

	default:
		AsConfig::Throw();
		break;
	}
}
